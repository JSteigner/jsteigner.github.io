////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
// I: single positive whole number
// O: true if even; false if odd
// C: make a recursive function
// E C: ensure ouput is a positive whole number
function isEven(x) {
      if ( x === 0) {
        return true;
      } else if ( x === 1) {
        return false;
      } else if ( x < 1) {
        return isEven(-x); 
      } else {
        return isEven(x-2);
      }
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
// I: string and a character
// O: a  number
// C: return number of times the character occurs in string
function countChars(string, character) {
     let characters = '';
     for(let i = 0; i < string.length; i ++) {
       if (string[i] === character) {
         characters += string[i];
       }
     }  return characters.length;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
// I: a string
// O: a number
// C: return a number that equals how many uppercase 'Bs' exist in string
function countBs(string) {
      let upperCaseBs = '';
       for(let i = 0; i < string.length; i ++) {
         if(string[i] === 'B') {
           upperCaseBs += string[i];
         }
       }  return upperCaseBs.length;
}

